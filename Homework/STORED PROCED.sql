--2
CREATE PROCEDURE ZOOGI(@NUM smallint)
AS
	BEGIN
	IF @NUM%2=0
		PRINT 'ZOOGI'
	ELSE
	PRINT 'NOT ZOOGI'
	END
GO
EXEC ZOOGI 7

--3
CREATE PROCEDURE SALARY(@NUM INT)
AS
	BEGIN

	SELECT FirstName
	FROM Employee_tbl E JOIN Employee_pay_tbl EP
	ON E.EmpID=EP.EmpID
	WHERE Salary<@NUM
	END
GO
EXEC SALARY 10000

